def export_to_obj(vertices, faces, vertex_colors=None, filename="face_reconstruction.obj"):
    """Export mesh data to OBJ format with optional vertex colors as comments."""
    obj_content = []
    
    # Add header
    obj_content.append(f"# 3D Face Reconstruction Export")
    obj_content.append(f"# Vertices: {len(vertices)}, Faces: {len(faces)}")
    obj_content.append(f"# Generated by 3D Face Reconstruction App")
    obj_content.append("")
    
    # Add vertices
    if vertex_colors is not None and len(vertex_colors) == len(vertices):
        # Include vertex colors as comments
        for i, (v, color) in enumerate(zip(vertices, vertex_colors)):
            obj_content.append(f"v {v[0]:.6f} {v[1]:.6f} {v[2]:.6f} # color({color[0]},{color[1]},{color[2]})")
    else:
        # Just vertices without color info
        for v in vertices:
            obj_content.append(f"v {v[0]:.6f} {v[1]:.6f} {v[2]:.6f}")
    
    obj_content.append("")
    
    # Add faces (OBJ uses 1-based indexing)
    for face in faces:
        # Add 1 to each index for OBJ format
        face_line = "f " + " ".join([str(idx + 1) for idx in face])
        obj_content.append(face_line)
    
    obj_content.append("")
    obj_content.append("# End of file")
    
    return "\n".join(obj_content)

def export_to_obj_with_mtl(vertices, faces, vertex_colors=None, filename="face_reconstruction.obj"):
    """Export mesh data to OBJ format with MTL file for vertex colors."""
    if vertex_colors is None or len(vertex_colors) != len(vertices):
        return export_to_obj(vertices, faces, None, filename)
    
    # Create MTL content
    mtl_content = []
    mtl_content.append("# Material file for 3D Face Reconstruction")
    mtl_content.append("newmtl FaceMaterial")
    mtl_content.append("Ka 1.000 1.000 1.000")  # Ambient
    mtl_content.append("Kd 1.000 1.000 1.000")  # Diffuse
    mtl_content.append("Ks 0.000 0.000 0.000")  # Specular
    mtl_content.append("")
    
    # Create OBJ content
    obj_content = []
    obj_content.append(f"# 3D Face Reconstruction Export with Colors")
    obj_content.append(f"# Vertices: {len(vertices)}, Faces: {len(faces)}")
    obj_content.append(f"mtllib {filename.replace('.obj', '.mtl')}")
    obj_content.append("usemtl FaceMaterial")
    obj_content.append("")
    
    # Add vertices
    for v in vertices:
        obj_content.append(f"v {v[0]:.6f} {v[1]:.6f} {v[2]:.6f}")
    
    obj_content.append("")
    
    # Add vertex colors as vertex normals (workaround for color in some viewers)
    for color in vertex_colors:
        # Normalize colors to [0,1] range
        r, g, b = color[0]/255.0, color[1]/255.0, color[2]/255.0
        obj_content.append(f"vn {r:.6f} {g:.6f} {b:.6f}")
    
    obj_content.append("")
    
    # Add faces with vertex normals
    for face in faces:
        face_line = "f " + " ".join([f"{idx+1}//{idx+1}" for idx in face])
        obj_content.append(face_line)
    
    obj_content.append("")
    obj_content.append("# End of file")
    
    return "\n".join(obj_content), "\n".join(mtl_content)

