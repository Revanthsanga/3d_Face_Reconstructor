def simplify_point_cloud_data(vertices, vertex_colors, target_vertices=5000):
    """Simplifies vertex data for a point cloud display by random sampling."""
    if len(vertices) <= target_vertices:
        return vertices, vertex_colors
    sample_indices = np.random.choice(len(vertices), target_vertices, replace=False)
    vertices_simple = vertices[sample_indices]
    if vertex_colors is not None and len(vertex_colors) == len(vertices):
        vertex_colors_simple = vertex_colors[sample_indices]
    else:
        vertex_colors_simple = None
    return vertices_simple, vertex_colors_simple

def create_3d_visualization(vertices, faces, vertex_colors, mesh_style, show_colors):
    """Create 3D visualization based on the selected style."""
    try:
        if mesh_style == "Realistic":
            return create_realistic_mesh(vertices, faces, vertex_colors, show_colors)
        elif mesh_style == "Point Cloud":
            return create_point_cloud(vertices, vertex_colors, show_colors)
        elif mesh_style == "Wireframe":
            return create_wireframe(vertices, faces)
        elif mesh_style == "Surface":
            return create_surface_mesh(vertices, faces, vertex_colors if show_colors else None)
        elif mesh_style == "Textured":
            return create_textured_mesh(vertices, faces, vertex_colors if show_colors else None)
        else:
            return create_point_cloud(vertices, vertex_colors, show_colors)
    except Exception as e:
        st.error(f"Visualization error in {mesh_style} mode: {e}")
        return create_point_cloud(vertices, vertex_colors, show_colors)

def create_realistic_mesh(vertices, faces, vertex_colors=None, show_colors=True):
    """Create a mesh with a realistic lighting model."""
    mesh_props = {
        'x': vertices[:, 0], 'y': vertices[:, 1], 'z': vertices[:, 2],
        'i': faces[:, 0], 'j': faces[:, 1], 'k': faces[:, 2],
        'opacity': 1.0,
        'flatshading': False,
        'lighting': {
            'ambient': 0.4,
            'diffuse': 1.0,
            'specular': 0.5,
            'roughness': 0.5,
            'fresnel': 0.2
        },
        'lightposition': {'x': 100, 'y': 200, 'z': 0}
    }

    if vertex_colors is not None and show_colors:
        mesh_props['vertexcolor'] = [f'rgb({c[0]},{c[1]},{c[2]})' for c in vertex_colors]
    else:
        # A neutral skin-like tone as a fallback
        mesh_props['color'] = 'rgb(224,172,144)'

    fig = go.Figure(data=[go.Mesh3d(**mesh_props)])
    
    fig.update_layout(scene=dict(
        xaxis=dict(visible=False),
        yaxis=dict(visible=False),
        zaxis=dict(visible=False)
    ))
    return fig

def create_point_cloud(vertices, vertex_colors, show_colors):
    """Create point cloud visualization."""
    marker_props = dict(size=2, opacity=0.8)
    if show_colors and vertex_colors is not None and len(vertex_colors) == len(vertices):
        marker_props['color'] = vertex_colors
    else:
        marker_props['color'] = 'lightblue'
    fig = go.Figure(data=[go.Scatter3d(
        x=vertices[:, 0], y=vertices[:, 1], z=vertices[:, 2],
        mode='markers', marker=marker_props, name='Face Points'
    )])
    return fig

def create_wireframe(vertices, faces):
    """Create an efficient wireframe visualization."""
    x_coords, y_coords, z_coords = [], [], []
    for face in faces:
        for i in range(3):
            v_start_idx, v_end_idx = face[i], face[(i + 1) % 3]
            x_coords.extend([vertices[v_start_idx][0], vertices[v_end_idx][0], None])
            y_coords.extend([vertices[v_start_idx][1], vertices[v_end_idx][1], None])
            z_coords.extend([vertices[v_start_idx][2], vertices[v_end_idx][2], None])
    fig = go.Figure(data=[go.Scatter3d(
        x=x_coords, y=y_coords, z=z_coords,
        mode='lines', line=dict(color='deepskyblue', width=1), name='Wireframe'
    )])
    return fig

def create_surface_mesh(vertices, faces, vertex_colors=None):
    """Create a solid surface mesh visualization."""
    mesh_props = {
        'x': vertices[:, 0], 'y': vertices[:, 1], 'z': vertices[:, 2],
        'i': faces[:, 0], 'j': faces[:, 1], 'k': faces[:, 2],
        'opacity': 0.9, 'flatshading': True
    }
    if vertex_colors is not None:
        mesh_props['vertexcolor'] = [f'rgb({c[0]},{c[1]},{c[2]})' for c in vertex_colors]
    else:
        mesh_props['color'] = 'lightpink'
    fig = go.Figure(data=[go.Mesh3d(**mesh_props)])
    return fig

def create_textured_mesh(vertices, faces, vertex_colors=None):
    """Create a textured mesh (identical to surface mesh with vertex colors)."""
    return create_surface_mesh(vertices, faces, vertex_colors)

def create_demo_face_mesh():
    """Create a simple demo face mesh for display when the model fails."""
    phi = np.linspace(0, np.pi, 30)
    theta = np.linspace(0, 2 * np.pi, 40)
    phi, theta = np.meshgrid(phi, theta)
    x = np.sin(phi) * np.cos(theta)
    y = np.sin(phi) * np.sin(theta) * 1.2
    z = np.cos(phi)
    nose_mask = (np.abs(y) < 0.2) & (x > 0.8)
    x[nose_mask] = x[nose_mask] * 1.15
    vertices = np.vstack([x.ravel(), y.ravel(), z.ravel()]).T
    faces = []
    rows, cols = phi.shape
    for r in range(rows - 1):
        for c in range(cols - 1):
            v1, v2, v3, v4 = r*cols+c, r*cols+(c+1), (r+1)*cols+c, (r+1)*cols+(c+1)
            faces.extend([[v1, v2, v4], [v1, v4, v3]])
    base_color = np.array([224, 172, 144])
    color_variation = np.random.randint(-15, 15, size=(len(vertices), 3))
    vertex_colors = np.clip(base_color + color_variation, 0, 255)
    return vertices, np.array(faces), vertex_colors

